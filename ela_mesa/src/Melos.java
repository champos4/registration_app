

public class Melos {
	
	private String id;
	private String onoma;

	public Melos(String id, String onoma) {
		
		this.id = id;
		this.onoma = onoma;
	}
	
	public String getOnoma() {
		return onoma;
	}
	public String getID() {
		return id;
	}
	public void setOnoma(String s) {
		onoma = s;
	}
	public void setID(String s) {
		id = s;
	}
	
	
}
